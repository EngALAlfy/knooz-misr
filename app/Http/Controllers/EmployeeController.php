<?php

namespace App\Http\Controllers;

use App\Models\Employee;
use App\Http\Requests\StoreEmployeeRequest;
use App\Http\Requests\UpdateEmployeeRequest;
use App\Models\EmployeeLine;
use Illuminate\Support\Facades\Auth;

class EmployeeController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function all()
    {
        //
        $employees = Employee::with('line')->orderByDesc('created_at')->get();
        return view('employees.all' , compact('employees'));

    }

    public function attendance()
    {
        //
        $employees = Employee::with('attendance')->orderByDesc('created_at')->get();
        return view('employees.attendance' , compact('employees'));

    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
        $lines = EmployeeLine::withCount('employees')->orderByDesc('created_at')->get();
        return view('employees.create' , compact('lines'));

    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreEmployeeRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreEmployeeRequest $request)
    {
        //
        $data = $request->validated();

        $data['create_user_id'] = Auth::user()->id;

        Employee::create($data);

        return redirect()->route('employees.all');
    }

